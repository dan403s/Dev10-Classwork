package com.dvb.fibonaccinumberswithrecursivemethod;

/**
 *
 * @author Daniel Bart
 */
public class FibonacciMethod {

    public int fib(int n) {
        if (n <= 1) {
            return n;
        } else {
            return fib(n - 1) + fib(n - 2);
        }
    }

}
